#include "3-calc.h"
/**
 * op_add - function that calculates sum of two integer
 * @a: first integer
 * @b: second integer
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that calculates differnece of two integers
 * @a: first integer
 * @b: second integer
 * Return: difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that divides two integers
 * @a: first integer
 * @b: second integer
 * Return: result of dividind a by b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that calculates remainder of two integers
 * @a: first integer
 * @b: second integer
 * Return: remainder of a divided by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
